// In programming, a loop is used to perform a task repeatedly.

// JavaScript has five types of loops, which vary based on the condition.

// 1. For Loop

// In JavaScript, the 'for' loop is used for iterating over a block of code a certain number of times,
// or for iterating over the elements of an array.

// Example: Printing numbers from 0 to 20

for (let i = 0; i <= 20; i++) {
    console.log("Number: ", i);
}

// This code will print numbers from 0 to 20

// Example: Iterating over the elements of an array

let students = ["Rahul", "Dharmik", "Utsav", "Vaishali", "Brij"]

for (let i = 0; i < students.length; i++) {
    console.log("Student Name: ", students[i]);
}

// This code will print each element in the 'students' array.