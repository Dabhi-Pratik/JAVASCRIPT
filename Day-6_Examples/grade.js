let marks = 75


// check grade based on marks
if (marks >= 90) {
    console.log("You have A+ grade")
} else if (marks >= 80) {
    console.log("You have A grade")
} else if (marks >= 70) {
    console.log("You have B grade")
} else if (marks >= 60) {
    console, log("You have C grade")
} else if (marks >= 35) {
    console.log("You have Passed this Exam")
} else {
    console.log("You have Failed this Exam")
}